#!/usr/bin/env node
// Generate the worker-side skill catalog from skills/<id>/{SKILL.md,TRYIT.md}.
//
// Produces:
//   src/generated/skill-prompts.js       — SKILL_PROMPTS, SKILL_NAMES, VALID_SKILLS
//
// Regenerated on every build; editing it by hand is wasted effort — change
// skills/<id>/SKILL.md frontmatter instead. The Astro site reads skills/
// directly through its content collection (see site/src/content.config.ts).
//
// Schema is validated strictly. A missing required field or an unknown
// phase fails the build; there are no silent fallbacks.

import { existsSync, mkdirSync, readFileSync, readdirSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import matter from "gray-matter";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");
const SKILLS_DIR = join(ROOT, "skills");

// Runtime knob surfaced to TRYIT.md templates. Keep in sync with the
// constant of the same name in src/opchain-try.js.
const MAX_EXCHANGES = 5;

const VALID_PHASES = new Set(["foundation", "plan", "build"]);
const REQUIRED_FIELDS = [
  "name",
  "displayName",
  "version",
  "shortDesc",
  "phases",
  "triAgent",
  "tryable",
  "commands",
  "description",
];

function listSkillDirs() {
  return readdirSync(SKILLS_DIR, { withFileTypes: true })
    .filter((d) => d.isDirectory())
    .map((d) => d.name)
    .filter((name) => existsSync(join(SKILLS_DIR, name, "SKILL.md")))
    .sort();
}

function parseSkill(id) {
  const skillPath = join(SKILLS_DIR, id, "SKILL.md");
  const raw = readFileSync(skillPath, "utf8");
  const { data, content } = matter(raw);

  for (const field of REQUIRED_FIELDS) {
    if (!(field in data)) {
      throw new Error(`skills/${id}/SKILL.md: missing required frontmatter field \`${field}\``);
    }
  }
  if (data.name !== id) {
    throw new Error(
      `skills/${id}/SKILL.md: frontmatter \`name: ${data.name}\` does not match directory name \`${id}\``,
    );
  }
  if (!Array.isArray(data.phases) || data.phases.length === 0) {
    throw new Error(`skills/${id}/SKILL.md: \`phases\` must be a non-empty array`);
  }
  for (const p of data.phases) {
    if (!VALID_PHASES.has(p)) {
      throw new Error(
        `skills/${id}/SKILL.md: unknown phase \`${p}\` (valid: ${[...VALID_PHASES].join(", ")})`,
      );
    }
  }
  if (typeof data.triAgent !== "boolean") {
    throw new Error(`skills/${id}/SKILL.md: \`triAgent\` must be a boolean`);
  }
  if (typeof data.tryable !== "boolean") {
    throw new Error(`skills/${id}/SKILL.md: \`tryable\` must be a boolean`);
  }
  if (!Array.isArray(data.commands)) {
    throw new Error(`skills/${id}/SKILL.md: \`commands\` must be an array (use [] for none)`);
  }

  let tryPrompt = null;
  const tryitPath = join(SKILLS_DIR, id, "TRYIT.md");
  if (data.tryable) {
    if (!existsSync(tryitPath)) {
      throw new Error(`skills/${id}: tryable:true but no TRYIT.md file found`);
    }
    tryPrompt = readFileSync(tryitPath, "utf8")
      .replace(/\{\{maxExchanges\}\}/g, String(MAX_EXCHANGES))
      .trimEnd();
  } else if (existsSync(tryitPath)) {
    throw new Error(`skills/${id}: tryable:false but a TRYIT.md file exists — pick one`);
  }

  return {
    id,
    displayName: data.displayName,
    version: data.version,
    shortDesc: data.shortDesc,
    phases: data.phases,
    triAgent: data.triAgent,
    tryable: data.tryable,
    commands: data.commands,
    description: data.description.trim(),
    tryPrompt,
    // Extract the first non-heading paragraph of SKILL.md body for the site's
    // skill detail page summary (Sprint 3 will consume this).
    excerpt: firstParagraph(content),
    body: content.trim(),
  };
}

function firstParagraph(markdown) {
  const lines = markdown.split(/\r?\n/);
  let i = 0;
  // Skip leading blank lines and any H1 block.
  while (i < lines.length && lines[i].trim() === "") i++;
  if (i < lines.length && lines[i].startsWith("# ")) {
    i++;
    while (i < lines.length && lines[i].trim() === "") i++;
  }
  const out = [];
  while (i < lines.length && lines[i].trim() !== "") {
    if (lines[i].startsWith("#")) break;
    out.push(lines[i].trim());
    i++;
  }
  return out.join(" ").trim();
}

function writeWorkerCatalog(skills) {
  const tryable = skills.filter((s) => s.tryable);
  const prompts = tryable
    .map((s) => `  ${JSON.stringify(s.id)}: ${JSON.stringify(s.tryPrompt)},`)
    .join("\n");
  const names = skills
    .map((s) => `  ${JSON.stringify(s.id)}: ${JSON.stringify(s.displayName)},`)
    .join("\n");
  const body = `/**
 * GENERATED by scripts/gen-skills-catalog.mjs — do not edit by hand.
 * Source of truth: skills/<id>/SKILL.md frontmatter + skills/<id>/TRYIT.md.
 */

export const SKILL_PROMPTS = {
${prompts}
};

export const SKILL_NAMES = {
${names}
};

export const VALID_SKILLS = Object.keys(SKILL_PROMPTS);
`;
  const outDir = join(ROOT, "src", "generated");
  mkdirSync(outDir, { recursive: true });
  writeFileSync(join(outDir, "skill-prompts.js"), body);
  console.log(
    `wrote src/generated/skill-prompts.js (${tryable.length} tryable, ${skills.length} display names)`,
  );
}

function main() {
  const ids = listSkillDirs();
  if (ids.length === 0) {
    throw new Error("no skills/ directories with a SKILL.md found");
  }
  const skills = ids.map(parseSkill);

  // Invariants we care about end-to-end, asserted here so the build fails
  // with a readable error instead of a silent downstream mismatch.
  const foundation = skills.filter((s) => s.phases.includes("foundation"));
  if (!foundation.some((s) => s.id === "checkpoint-protocol")) {
    throw new Error("invariant: checkpoint-protocol must be present with phases:[foundation]");
  }
  const tryableIds = skills.filter((s) => s.tryable).map((s) => s.id);
  if (tryableIds.includes("checkpoint-protocol")) {
    throw new Error("invariant: checkpoint-protocol cannot be tryable");
  }

  // Sprint 6: the old static public/skills.html → public/skills.js pipeline
  // is gone; the Astro content collection reads skills/ directly. Only the
  // worker-side prompt catalog still needs generating.
  writeWorkerCatalog(skills);

  console.log(
    `✓ catalog generated: ${skills.length} skills (${tryableIds.length} tryable) — ${tryableIds.join(", ")}`,
  );
}

main();
